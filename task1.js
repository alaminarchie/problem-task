// 1. প্রাথমিক ব্যালেন্স বা টাকার পরিমাণ
let balance = 1000; // তোমার কাছে ১০০০ টাকা আছে

// 2. কমলার দাম
let costOrange = 400; // ১ কেজি কমলার দাম ৪০০ টাকা

// 3. আপেলের দাম
let costApple = 300; // ১ কেজি আপেলের দাম ৩০০ টাকা

// 4. ফলের মোট খরচ বের করো
let costOfFruits = costOrange + costApple; // ৪০০ + ৩০০ = ৭০০ টাকা

// 5. ফেরত পাওয়া টাকা বের করো
let moneyReturn = balance - costOfFruits; // ১০০০ - ৭০০ = ৩০০ টাকা ফেরত পাবে

// 6. কনসোলে ফলাফল প্রিন্ট করো
console.log(moneyReturn); // আউটপুট: ৩০০ টাকা
